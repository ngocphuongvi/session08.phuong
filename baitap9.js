let a = 5; //a có giá trị là 5 và kiểu dữ liệu là number
let b = "5"; //b cũng có giá trị là 5 nhưng kiểu dữ liệu là string

// == : so sánh bằng theo giá trị
// === : so sánh bằng theo cả giá trị và kiểu dữ liệu
// != : so sánh không bằng theo giá trị
// !== : so sánh không bằng theo cả giá trị và kiểu dữ liệu

console.log(a == b); //true : a và b có cùng giá trị
console.log(a === b); // false : a và b khác kiểu dữ liệu nên không thể bằng nhau tuyệt đối
console.log(a != b); // false : về mặt giá trị a và b bằng nhau
console.log(a !== b); // true : a và b khác nhau ở kiểu dữ liệu nên a khác b




